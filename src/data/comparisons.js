// Helper functions
export function getComparisonById(id) {
  return comparisons.find(c => c.id === id);
}

export function getComparisonsByCategory(category) {
  return comparisons.filter(c => c.category === category);
}

export function calculateQuantity(itemCost) {
  return Math.floor(BOM_TOTAL_COST / itemCost);
}

export function getAllCategories() {
  return Object.keys(categories).map(key => ({
    slug: key,
    ...categories[key]
  }));
}

export function getRandomComparison() {
  return comparisons[Math.floor(Math.random() * comparisons.length)];
}

